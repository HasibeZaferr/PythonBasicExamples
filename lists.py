list = [ 'abcd', 786 , 2.23, 'john', 70.2 ]
tinylist = [123, 'john']

print (list)          # list adındaki listenin tamamını yazar
print (list[0])       # list adındaki listenin ilk elemanını yazar
print (list[1:3])     # list adındaki listenin 2'den 3'e kadar olan elemanlarını yazar
print (list[2:])      # list adındaki listenin 3. elemanından başlayarak tamamını yazar
print (tinylist * 2)  # tinylist adındaki listeyi iki kere yazar
print (list + tinylist) # list ve tinylist listesini birleştirerek yazar