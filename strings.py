# -*- coding: utf-8 -*-

str = 'Hello World!'

print (str)          # Stringin tamamını yazar
print (str[0])       # Stringin ilk karakterini yazar
print (str[1:4])     # Stringin 2. karakterinden 4. karakterine kadar olan kısmı yazar.(2-3-4)
print (str[2:])      # 3. karakterden başlayarak stringin tamamını yazar
print (str * 2)      # Stringi iki kere yazdırır
print (str + "Test") # Birleştirilmiş stringi yazar